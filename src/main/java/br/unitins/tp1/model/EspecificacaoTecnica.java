package br.unitins.tp1.model;

public class EspecificacaoTecnica {
    private double ph;
    private double opacidade;
    private double absorcao;

    public double getPh() {
        return ph;
    }

    public void setPh(double ph) {
        this.ph = ph;
    }

    public double getOpacidade() {
        return opacidade;
    }

    public void setOpacidade(double opacidade) {
        this.opacidade = opacidade;
    }

    public double getAbsorcao() {
        return absorcao;
    }

    public void setAbsorcao(double absorcao) {
        this.absorcao = absorcao;
    }
}
